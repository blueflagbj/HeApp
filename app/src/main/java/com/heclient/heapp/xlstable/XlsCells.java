package com.heclient.heapp.xlstable;

public class XlsCells {
    private int rowx;
    private int rowy;
    private String cellvalue;

    public int getRowx() {
        return rowx;
    }

    public void setRowx(int rowx) {
        this.rowx = rowx;
    }

    public int getRowy() {
        return rowy;
    }

    public void setRowy(int rowy) {
        this.rowy = rowy;
    }

    public String getCellValue() {
        return cellvalue;
    }

    public void setCellValue(String rowvalue) {
        this.cellvalue = rowvalue;
    }
}
